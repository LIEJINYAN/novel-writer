"""反 AI 检测规范 - 降低 AI 生成内容的可检测性。"""

# 反 AI 检测规范文本
ANTI_AI_GUIDELINES = """写作规范要求（必须遵守）：
1. 【词汇多样性】避免重复使用相同的句式开头。不要连续多个句子以"他"、"她"、"它"或角色名开头。交替使用主语、状语、介词短语开头。
2. 【节奏变化】长短句交替使用。不要所有句子长度相近。重要情节用短句制造紧张感，描写用长句营造氛围。对话与叙述穿插。
3. 【具体细节】使用具体的感官描写（视觉、听觉、嗅觉、触觉、味觉），避免抽象概括。不说"他很生气"，而说"他的拳头攥得发白"。
4. 【个性化表达】每个角色的对话应有独特的语气、用词习惯和句式特点。避免所有角色说话方式相同。
5. 【避免完美结构】不要每个段落都有标准的"主题句+支撑句+总结句"结构。适当使用不完整的句子、插入语、省略号。
6. 【情感层次】不要直接陈述情感，通过动作、环境、对话间接表现。不只描写表面情绪，要有层次和矛盾。
7. 【段落长度变化】段落长度有变化，从一句话到多句话交替。不要所有段落长度相近。"""


def get_anti_ai_prompt(enabled: bool = True) -> str:
    """获取反 AI 检测规范文本。
    
    Args:
        enabled: 是否启用反 AI 检测
    Returns:
        str: 规范文本（如果禁用则返回空字符串）
    """
    return ANTI_AI_GUIDELINES if enabled else ""
